#!/usr/bin/env node
// Transform OurAirports' airports.csv into lib/data/airports.ts.
//
// Usage:
//   1. curl https://davidmegginson.github.io/ourairports-data/airports.csv -o data/ourairports.csv
//   2. node scripts/build-airports.mjs
//
// Filter rules:
//   - large_airport globally (must have an IATA code)
//   - medium_airport in continent EU (must have an IATA code)
//
// Output is sorted by city name (stable, deterministic).

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const CSV_PATH = resolve(ROOT, "data/ourairports.csv");
const OUT_PATH = resolve(ROOT, "lib/data/airports.ts");

if (!existsSync(CSV_PATH)) {
  console.error(`CSV not found at ${CSV_PATH}.`);
  console.error("Download with:");
  console.error("  curl https://davidmegginson.github.io/ourairports-data/airports.csv -o data/ourairports.csv");
  process.exit(1);
}

// Quote-aware CSV parser. OurAirports uses RFC 4180-ish format with quoted fields
// containing commas and "" for embedded quotes.
function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;

  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') {
          field += '"';
          i++;
        } else {
          inQuotes = false;
        }
      } else {
        field += c;
      }
    } else {
      if (c === '"') {
        inQuotes = true;
      } else if (c === ",") {
        row.push(field);
        field = "";
      } else if (c === "\n") {
        row.push(field);
        rows.push(row);
        row = [];
        field = "";
      } else if (c === "\r") {
        // skip; handled by \n
      } else {
        field += c;
      }
    }
  }
  if (field !== "" || row.length > 0) {
    row.push(field);
    rows.push(row);
  }
  return rows;
}

const csv = readFileSync(CSV_PATH, "utf8");
const rows = parseCsv(csv);
const header = rows.shift();
if (!header) {
  console.error("Empty CSV.");
  process.exit(1);
}

const idx = Object.fromEntries(header.map((h, i) => [h, i]));
const required = ["type", "iata_code", "name", "municipality", "iso_country", "continent"];
for (const k of required) {
  if (idx[k] === undefined) {
    console.error(`CSV missing column: ${k}`);
    process.exit(1);
  }
}

const countryNames = new Intl.DisplayNames(["en"], { type: "region" });

const airports = [];
for (const row of rows) {
  if (row.length < header.length) continue;
  const type = row[idx.type];
  const iata = row[idx.iata_code];
  const continent = row[idx.continent];

  if (!iata) continue;

  const isLargeAirport = type === "large_airport";
  const isEuMedium = type === "medium_airport" && continent === "EU";
  if (!isLargeAirport && !isEuMedium) continue;

  const city = row[idx.municipality];
  const isoCountry = row[idx.iso_country];
  if (!city || !isoCountry) continue;

  let country;
  try {
    country = countryNames.of(isoCountry) ?? isoCountry;
  } catch {
    country = isoCountry;
  }

  airports.push({
    iata,
    city,
    name: row[idx.name],
    country,
    countryCode: isoCountry,
  });
}

// De-duplicate on iata (in rare cases CSV may have variants).
const seen = new Set();
const unique = [];
for (const a of airports) {
  if (seen.has(a.iata)) continue;
  seen.add(a.iata);
  unique.push(a);
}

unique.sort((a, b) => a.city.localeCompare(b.city));

const banner = `// Generated by scripts/build-airports.mjs from data/ourairports.csv.
// Do not edit by hand. Re-run the script to regenerate.
// Filter: large_airport (worldwide) + medium_airport (Europe), with IATA code.
// Total: ${unique.length} airports.\n\n`;

const typeDecl = `export type Airport = {
  iata: string;
  city: string;
  name: string;
  country: string;
  countryCode: string;
  aka?: string[];
};\n\n`;

const arrayDecl =
  `export const AIRPORTS: Airport[] = [\n` +
  unique
    .map((a) => {
      const esc = (s) => JSON.stringify(s);
      return `  { iata: ${esc(a.iata)}, city: ${esc(a.city)}, name: ${esc(a.name)}, country: ${esc(a.country)}, countryCode: ${esc(a.countryCode)} },`;
    })
    .join("\n") +
  `\n];\n`;

writeFileSync(OUT_PATH, banner + typeDecl + arrayDecl);

console.log(`Wrote ${unique.length} airports → ${OUT_PATH}`);
